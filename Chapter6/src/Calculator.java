public class Calculator {

  //Method
  void powerOn(){
    System.out.println("환영합니다~!");
    System.out.println();
  }

  int plus (int x, int y) {
    return  x + y;
  }

  double divide (int x, int y) {
    return (double)x / (double)y;
  }

  void powerOff() {
    System.out.println("이용해 주셔서 감사합니디.");
    System.out.println();
  }
}
